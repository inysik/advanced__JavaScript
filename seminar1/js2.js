// Создаем карту для хранения поваров и их специализаций
const chefsMap = new Map([
    ['Виктор', 'Пицца'],
    ['Ольга', 'Суши'],
    ['Дмитрий', 'Десерты']
]);

// Создаем карту для хранения блюд и соответствующих поваров
const dishesMap = new Map([
    ['Пицца "Маргарита"', 'Виктор'],
    ['Пицца "Пепперони"', 'Виктор'],
    ['Суши "Филадельфия"', 'Ольга'],
    ['Суши "Калифорния"', 'Ольга'],
    ['Тирамису', 'Дмитрий'],
    ['Чизкейк', 'Дмитрий']
]);

// Создаем карту для хранения заказов каждого клиента
const ordersMap = new Map([
    ['Алексей', ['Пицца "Пепперони"', 'Тирамису']],
    ['Мария', ['Суши "Калифорния"', 'Пицца "Маргарита"']],
    ['Ирина', ['Чизкейк']]
]);

// Выводим информацию о заказах
ordersMap.forEach((dishes, client) => {
    console.log(`Заказ клиента ${client}:`);
    dishes.forEach(dish => {
        const chef = dishesMap.get(dish);
        console.log(`- ${dish}, готовит: ${chef}`);
    });
});
